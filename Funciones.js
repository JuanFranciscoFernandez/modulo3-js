function sumaTres(x) {
    console.log(x + 3);
}
sumaTres(5);

//formas de escribir la misma funcion:

//primera forma
function sumaTres(x) {
    return x + 3;
}

//segunda forma
var sumaTres = function (x) {
    return x + 3;
}

//Tercera Forma (funcion de flecha)
var sumaTres = (X) => {
    return X + 3;
}

